-- MIGRATION SCRIPT TO SUPABASE POSTGRESQL
-- This script creates the identical tables and sets up Row Level Security (RLS)

-- 1. Create Tables
CREATE TABLE IF NOT EXISTS public.admin_users (
    id bigint generated by default as identity primary key,
    username text not null,
    password text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

CREATE TABLE IF NOT EXISTS public.clients (
    id bigint generated by default as identity primary key,
    name text not null,
    email text,
    phone text,
    address text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

CREATE TABLE IF NOT EXISTS public.documents (
    id bigint generated by default as identity primary key,
    type text not null, -- 'Factura', 'Presupuesto', 'Nota de Entrega'
    client_name text not null,
    client_id bigint references public.clients(id) on delete set null,
    date date not null,
    total numeric(10, 2) not null,
    file_path text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

CREATE TABLE IF NOT EXISTS public.financial_records (
    id bigint generated by default as identity primary key,
    type text not null, -- 'Ingreso', 'Egreso'
    amount numeric(10, 2) not null,
    currency text not null, -- 'USD', 'VES'
    payment_method text not null, -- 'Banco', 'Efectivo', 'Binance'
    description text not null,
    document_id bigint references public.documents(id) on delete cascade,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

CREATE TABLE IF NOT EXISTS public.document_items (
    id bigint generated by default as identity primary key,
    document_id bigint not null references public.documents(id) on delete cascade,
    description text not null,
    quantity numeric(10, 2) not null,
    unit text not null,
    price_usd numeric(10, 2) not null,
    total_usd numeric(10, 2) not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

